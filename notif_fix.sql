--     알 수 없는 옛날 알림을 정리합니다(어떤 알림인지/어디로 가는지 모르는 것).
--   - 앞으로의 알림은 bid_notify.sql / quote_notify.sql 트리거가
--     "제목 + 어떤 시계 / 얼마 + 바로가기(ref_id)" 를 항상 채웁니다.
--
-- ※ 이 파일 실행 후, 아직 안 했다면 quote_notify.sql 과 bid_notify.sql 도 실행하세요.
