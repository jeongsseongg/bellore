-- 0) 알림 테이블/참조컬럼 보장(신규 환경 대비, 이미 있으면 무시)
create table if not exists public.notifications (
  id          bigint generated by default as identity primary key,
  user_id     uuid not null references auth.users(id) on delete cascade,
  type        text not null default 'info',
  title       text not null default '알림',
  body        text,
  is_read     boolean not null default false,
  created_at  timestamptz not null default now()
);
alter table public.notifications add column if not exists ref_id text;

-- 1) 알 수 없는 옛날 알림 정리
--    - 알려진 종류가 아닌 것(type 이 아래 목록에 없음) 이면서
--    - 본문이 비어 있거나 8자 미만으로 무슨 알림인지 알 수 없는 것
delete from public.notifications n
 where coalesce(n.type,'') not in
   ('quote_open','quote_new','bid_new','awarded','approved',
    'account','business','listing','settlement','support_new','support_reply','info')
   and (n.body is null or char_length(trim(n.body)) < 8);

-- 2) (선택) 본문이 비어 제목만 있는 알림에 기본 안내 채우기
update public.notifications
   set body = '내역을 확인해 주세요.'
 where (body is null or trim(body) = '')
   and title is not null and trim(title) <> '';
